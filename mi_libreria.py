def producto(n1, n2):
    """
    Calculate the product of two numbers.

    :param n1: The base number
    :type n1: int
    :param n2: The exponent number
    :type n2: int
    :returns: The result of base raised to the power of exponent.
    :rtype: int
    """
    return n1 * n2


